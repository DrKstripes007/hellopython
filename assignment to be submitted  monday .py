#This is to Calculate Average of 3 Numbers
print("Welcome to Average Calculator")
num1 = int(input("Enter first Value: "))
num2 = int(input("Enter the second value: "))
num3 = int(input("Enter the third value: "))

#Calculate total of number
total_number = num1 + num2 + num3

#Calculate the average mean
average_mean = total_number / 3

final_average_mean = round(average_mean, 2)
print(f"The average is {final_average_mean}")







